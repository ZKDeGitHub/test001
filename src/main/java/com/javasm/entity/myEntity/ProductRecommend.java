package com.javasm.entity.myEntity;


public class ProductRecommend {

  private long recommendId;
  private String popularDegree;
  private String firstRoundStatus;
  private String onlinePurchaseStatus;
  private String customerVisibleStatus;
  private String recommendReason;

  public ProductRecommend(long recommendId, String popularDegree, String firstRoundStatus, String onlinePurchaseStatus, String customerVisibleStatus, String recommendReason) {
    this.recommendId = recommendId;
    this.popularDegree = popularDegree;
    this.firstRoundStatus = firstRoundStatus;
    this.onlinePurchaseStatus = onlinePurchaseStatus;
    this.customerVisibleStatus = customerVisibleStatus;
    this.recommendReason = recommendReason;
  }

  public ProductRecommend() {
  }

  @Override
  public String toString() {
    return "ProductRecommend{" +
            "recommendId=" + recommendId +
            ", popularDegree='" + popularDegree + '\'' +
            ", firstRoundStatus='" + firstRoundStatus + '\'' +
            ", onlinePurchaseStatus='" + onlinePurchaseStatus + '\'' +
            ", customerVisibleStatus='" + customerVisibleStatus + '\'' +
            ", recommendReason='" + recommendReason + '\'' +
            '}';
  }

  public long getRecommendId() {
    return recommendId;
  }

  public void setRecommendId(long recommendId) {
    this.recommendId = recommendId;
  }


  public String getPopularDegree() {
    return popularDegree;
  }

  public void setPopularDegree(String popularDegree) {
    this.popularDegree = popularDegree;
  }


  public String getFirstRoundStatus() {
    return firstRoundStatus;
  }

  public void setFirstRoundStatus(String firstRoundStatus) {
    this.firstRoundStatus = firstRoundStatus;
  }


  public String getOnlinePurchaseStatus() {
    return onlinePurchaseStatus;
  }

  public void setOnlinePurchaseStatus(String onlinePurchaseStatus) {
    this.onlinePurchaseStatus = onlinePurchaseStatus;
  }


  public String getCustomerVisibleStatus() {
    return customerVisibleStatus;
  }

  public void setCustomerVisibleStatus(String customerVisibleStatus) {
    this.customerVisibleStatus = customerVisibleStatus;
  }


  public String getRecommendReason() {
    return recommendReason;
  }

  public void setRecommendReason(String recommendReason) {
    this.recommendReason = recommendReason;
  }

}
