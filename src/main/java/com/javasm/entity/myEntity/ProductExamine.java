package com.javasm.entity.myEntity;


import java.util.Date;

public class ProductExamine {

  private long productExamineId;
  private long productBasicId;
  private long userId;
  private Date createdTime;
  private Date lastModifyTime;

  public ProductExamine(long productExamineId, long productBasicId, long userId, Date createdTime, Date lastModifyTime) {
    this.productExamineId = productExamineId;
    this.productBasicId = productBasicId;
    this.userId = userId;
    this.createdTime = createdTime;
    this.lastModifyTime = lastModifyTime;
  }

  public ProductExamine() {
  }

  @Override
  public String toString() {
    return "ProductExamine{" +
            "productExamineId=" + productExamineId +
            ", productBasicId=" + productBasicId +
            ", userId=" + userId +
            ", createdTime=" + createdTime +
            ", lastModifyTime=" + lastModifyTime +
            '}';
  }

  public long getProductExamineId() {
    return productExamineId;
  }

  public void setProductExamineId(long productExamineId) {
    this.productExamineId = productExamineId;
  }

  public long getProductBasicId() {
    return productBasicId;
  }

  public void setProductBasicId(long productBasicId) {
    this.productBasicId = productBasicId;
  }

  public long getUserId() {
    return userId;
  }

  public void setUserId(long userId) {
    this.userId = userId;
  }

  public Date getCreatedTime() {
    return createdTime;
  }

  public void setCreatedTime(Date createdTime) {
    this.createdTime = createdTime;
  }

  public Date getLastModifyTime() {
    return lastModifyTime;
  }

  public void setLastModifyTime(Date lastModifyTime) {
    this.lastModifyTime = lastModifyTime;
  }
}
