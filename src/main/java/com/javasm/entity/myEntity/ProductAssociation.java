package com.javasm.entity.myEntity;


public class ProductAssociation {

  private long productAssociationId;
  private long productBasicId;
  private long productBasicAssociationId;


  public ProductAssociation(long productAssociationId, long productBasicId, long productBasicAssociationId) {
    this.productAssociationId = productAssociationId;
    this.productBasicId = productBasicId;
    this.productBasicAssociationId = productBasicAssociationId;
  }

  public ProductAssociation() {
  }

  @Override
  public String toString() {
    return "ProductAssociation{" +
            "productAssociationId=" + productAssociationId +
            ", productBasicId=" + productBasicId +
            ", productBasicAssociationId=" + productBasicAssociationId +
            '}';
  }

  public long getProductAssociationId() {
    return productAssociationId;
  }

  public void setProductAssociationId(long productAssociationId) {
    this.productAssociationId = productAssociationId;
  }


  public long getProductBasicId() {
    return productBasicId;
  }

  public void setProductBasicId(long productBasicId) {
    this.productBasicId = productBasicId;
  }


  public long getProductBasicAssociationId() {
    return productBasicAssociationId;
  }

  public void setProductBasicAssociationId(long productBasicAssociationId) {
    this.productBasicAssociationId = productBasicAssociationId;
  }

}
